export function lvBG(lv) {
  switch (lv) {
    case 0:
      return "forestBg";
    case 1:
      return "stoneBg";
    case 2:
      return "desertBg";
    case 3:
      return "iceBg";
    case 4:
      return "jungBg";
  }
}

export function terrain(lv) {
  switch (lv) {
    case 0:
      return "plain";
    case 1:
      return "stone";
    case 2:
      return "sand";
    case 3:
      return "ice";
    case 4:
      return "jungle";
  }
}
